package net.efaber.irekia.models;

import java.util.Comparator;

/** Implements a sorter of Content_item based on the sort_id attribute.
 */
public class Content_item_comparator
	implements Comparator<Content_item>
{
	@Override
	public int compare(Content_item o1, Content_item o2)
	{
		final long equal = o2.sort_id - o1.sort_id;
		if (0 == equal)
			return o2.id - o1.id;
		else if (equal < 0)
			return -1;
		else
			return +1;
	}
}
